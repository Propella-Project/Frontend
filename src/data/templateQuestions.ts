// Function to get template questions for a subject
export function getTemplateQuestions(subjectId: string, count: number = 3): Question[] {
  const normalizedId = subjectId.toLowerCase();
  const questions = templateDiagnosticQuestions[normalizedId] || [];
  
  // Shuffle and return requested count
  const shuffled = [...questions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}

// Function to get mixed template questions for multiple subjects
export function getMixedTemplateQuestions(subjectIds: string[], questionsPerSubject: number = 3): Question[] {
  const allQuestions: Question[] = [];
  
  subjectIds.forEach((subjectId, index) => {
    const questions = getTemplateQuestions(subjectId, questionsPerSubject);
    // Update IDs to ensure uniqueness across subjects
    questions.forEach((q, qIndex) => {
      allQuestions.push({
        ...q,
        id: `${subjectId}_template_${index}_${qIndex}_${Date.now()}`,
      });
    });
  });
  
  // Shuffle all questions
  return allQuestions.sort(() => Math.random() - 0.5);
}
